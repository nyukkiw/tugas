    private void prosesPembayaran(String kamar, Date checkInDate, Date checkOutDate) {
        int hargaKamar = getHargaKamar(kamarTerpilih);
        long lamaMenginap = (checkOutDate.getTime() - checkInDate.getTime()) / (1000 * 60 * 60 * 24); // Menghitung lama menginap dalam hari

        System.out.println("Kamar: " + kamar);
        System.out.println("Harga Kamar: " + hargaKamar);
        System.out.println("Lama Menginap: " + lamaMenginap);
        System.out.println(" ");
        
        
        if (lamaMenginap > 0) {
            int totalBiaya = (int) (lamaMenginap * hargaKamar);
            System.out.println("Total Biaya: " + totalBiaya);
            totalBiayaField.setText(String.valueOf(totalBiaya));
            if (totalBiaya > 0) {
                JOptionPane.showMessageDialog(this, "Total biaya untuk " + kamar + " adalah: " + totalBiaya, "Pembayaran", JOptionPane.INFORMATION_MESSAGE);
                
            } else {
                JOptionPane.showMessageDialog(this, "Total biaya tidak boleh nol.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Tanggal check-out harus setelah tanggal check-in.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        totalBiayaField.setEditable(false);
    }
    
    private int getHargaKamar(String kamar) {
        switch (kamar) {
            case "001":
                return 250000;
            case "002":
                return 250000; // Pastikan harga ditetapkan
            case "003":
                return 250000;
            case "004":
                return 350000;
            case "005":
                return 350000;
            case "006":
                return 350000;
            case "007":
                return 500000;
            case "008":
                return 500000;
            case "009":
                return 500000;
            default:
                return 0; // Pastikan tidak ada kamar yang tidak terdaftar
        }
    }

