    private void qrisButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_qrisButtonActionPerformed
        
        JFrame qrFrame = new JFrame("QRIS Payment");
        qrFrame.setSize(300, 300);
        qrFrame.setLayout(new java.awt.BorderLayout());
        
        JLabel qrLabel = new JLabel("QR CODE AREA", SwingConstants.CENTER);
        qrLabel.setBorder(BorderFactory.createTitledBorder("Silakan Scan QR"));
        
        JLabel timerLabel = new JLabel("Timer: 0:60", SwingConstants.CENTER);
        
        qrFrame.add(qrLabel, java.awt.BorderLayout.CENTER);
        qrFrame.add(timerLabel, java.awt.BorderLayout.SOUTH);
        
        qrFrame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        qrFrame.setVisible(true);
        
        Timer timer = new Timer();
        final int[] timeLeft = {10};
        
        TimerTask task = new TimerTask() {
            @Override
            public void run() {
                timeLeft[0]--;
                timerLabel.setText("Timer: " + (timeLeft[0] / 60) + ":" + String.format("%02d", timeLeft[0] % 60));
                
                if (timeLeft[0] <= 0) {
                    timer.cancel();
                    timerLabel.setText("Waktu habis. Silakan ulangi.");
                    qrFrame.dispose();
                    JOptionPane.showMessageDialog(null,
                            "Waktu pembayaran sudah habis. silahkan ulangi pembayarannya", "peringatan!!!", JOptionPane.WARNING_MESSAGE);
                }
            }
        };
        
        timer.scheduleAtFixedRate(task, 0, 1000);
    }//GEN-LAST:event_qrisButtonActionPerformed

